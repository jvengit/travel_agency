CREATE TABLE BOOKING (
    ID NUMBER(10, 0) GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    CATEGORY VARCHAR2(10 CHAR),
    NAME VARCHAR2(10 CHAR),
    PRICE_PER_NIGHT NUMBER(19, 0),
    ROOM_TYPE VARCHAR2(10 CHAR),
    SERVICE_TYPE VARCHAR2(10 CHAR),
    STATUS NUMBER(10, 0)
);


