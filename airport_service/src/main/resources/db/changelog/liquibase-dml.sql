CREATE TABLE FLIGHT (
	ID NUMBER(10,0) GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
	AIRLINE VARCHAR2(10) NULL,
	ARRIVAL TIMESTAMP NULL,
	DEPARTURE TIMESTAMP NULL,
	DESTINATION VARCHAR2(10) NULL,
	FLIGHT VARCHAR2(10) NULL,
	GATE VARCHAR2(10) NULL,
	ORIGIN VARCHAR2(10) NULL,
	STATUS NUMBER(10,0) NULL
);

